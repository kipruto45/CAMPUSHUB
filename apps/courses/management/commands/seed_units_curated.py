class Command(BaseCommand):
    help = "Seed units for selected courses using curated data."

    def handle(self, *args, **options):
        created = 0
        updated = 0
        missing_courses = []

        for course_code, entries in COURSE_UNITS.items():
            course = Course.objects.filter(code=course_code).first()
            if not course:
                missing_courses.append(course_code)
                continue

            for year, sem, code, name in entries:
                sem_str = str(sem)
                target_slug = slugify(f"{course.code}-{code}-s{sem_str}")

                # First, consolidate by slug globally (handles whitespace/case variations).
                dupes_by_slug = list(
                    Unit.objects.filter(slug=target_slug).order_by("created_at", "id")
                )
                if dupes_by_slug:
                    primary = dupes_by_slug[0]
                    if len(dupes_by_slug) > 1:
                        Unit.objects.filter(id__in=[u.id for u in dupes_by_slug[1:]]).delete()

                    primary.course = course
                    primary.code = code
                    primary.name = name
                    primary.year_of_study = year
                    primary.semester = sem_str
                    primary.is_active = True
                    primary.slug = target_slug
                    primary.save(
                        update_fields=[
                            "course",
                            "code",
                            "name",
                            "year_of_study",
                            "semester",
                            "is_active",
                            "slug",
                            "updated_at",
                        ]
                    )
                    updated += 1
                    continue

                # Otherwise, upsert strictly on (course, code, semester).
                obj, was_created = Unit.objects.get_or_create(
                    course=course,
                    code=code,
                    semester=sem_str,
                    defaults={
                        "name": name,
                        "year_of_study": year,
                        "is_active": True,
                        "slug": target_slug,
                    },
                )
                if was_created:
                    created += 1
                else:
                    obj.name = name
                    obj.year_of_study = year
                    obj.is_active = True
                    obj.slug = target_slug
                    obj.save(
                        update_fields=["name", "year_of_study", "is_active", "slug", "updated_at"]
                    )
                    updated += 1

        self.stdout.write(
            self.style.SUCCESS(
                f"Units processed. Created: {created}, Updated: {updated}"
            )
        )
        if missing_courses:
            self.stdout.write(
                self.style.WARNING(
                    "Courses not found (skipped): " + ", ".join(sorted(set(missing_courses)))
                )
            )
