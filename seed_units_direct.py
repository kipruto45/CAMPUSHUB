def clean_unit_code(code):
    """Clean unit code - remove semicolons, periods, etc."""
    return code.strip().rstrip(';').rstrip('.').strip()

def parse_unit_data(data):
    """Parse the unit data string into structured format"""
    courses = {}
    
    # Split by course (each line starts with course code)
    lines = data.strip().split('\n')
    for line in lines:
        line = line.strip()
        if not line:
            continue
        
        # Extract course code
        match = re.match(r'^([A-Z]+):', line)
        if not match:
            continue
        
        course_code = match.group(1)
        rest = line[match.end():].strip()
        
        units_by_year_sem = {}
        
        # Find all Yx Sx patterns
        year_sem_pattern = r'(Y\d+)\s*S(\d+)\s*([^Y]+)'
        matches = re.finditer(year_sem_pattern, rest)
        
        for m in matches:
            year = int(m.group(1)[1])  # Y1 -> 1
            semester = m.group(2)  # S2 -> 2
            unit_codes_str = m.group(3).strip()
            
            # Parse unit codes (comma separated) and clean them
            unit_codes = [clean_unit_code(u) for u in unit_codes_str.split(',') if clean_unit_code(u)]
            
            key = (year, semester)
            if key not in units_by_year_sem:
                units_by_year_sem[key] = []
            units_by_year_sem[key].extend(unit_codes)
        
        courses[course_code] = units_by_year_sem
    
    return courses

def seed_units():
    conn = psycopg2.connect(DATABASE_URL)
    cur = conn.cursor()
    
    print("Connected to database!")
    
    # Get all courses with their codes
    cur.execute("SELECT code, id FROM courses_course")
    course_codes = {row[0]: row[1] for row in cur.fetchall()}
    print(f"Found {len(course_codes)} courses: {list(course_codes.keys())}")
    
    # Check if BASE exists
    if 'BASE' not in course_codes:
        print("Warning: BASE not found. Available courses:", list(course_codes.keys()))
    
    # Parse unit data
    units_data = parse_unit_data(UNIT_DATA)
    print(f"Parsed {len(units_data)} courses with units")
    
    units_created = 0
    units_skipped = 0
    errors = 0
    
    for course_code, year_sem_units in units_data.items():
        if course_code not in course_codes:
            print(f"Warning: Course {course_code} not found in database!")
            continue
        
        course_id = course_codes[course_code]
        
        for (year, semester), unit_codes in year_sem_units.items():
            for unit_code in unit_codes:
                # Check if unit exists (by course, code, and semester)
                cur.execute("""
                    SELECT id FROM courses_unit 
                    WHERE course_id = %s AND code = %s AND semester = %s
                """, (course_id, unit_code, semester))
                
                if cur.fetchone():
                    units_skipped += 1
                    continue
                
                # Create unit - use code as name (could be enhanced with lookup table)
                unit_name = unit_code  # Using code as placeholder name
                
                try:
                    # Create unique slug including year and semester
                    slug = f"{course_code.lower()}-{unit_code.lower()}-y{year}s{semester}"
                    
                    cur.execute("""
                        INSERT INTO courses_unit 
                        (id, course_id, name, code, description, slug, semester, year_of_study, is_active, created_at, updated_at)
                        VALUES (%s, %s, %s, %s, '', %s, %s, %s, true, NOW(), NOW())
                        RETURNING id
                    """, (str(uuid.uuid4()), course_id, unit_name, unit_code, 
                          slug, semester, year))
                    
                    units_created += 1
                    print(f"Created: {course_code} - {unit_code} (Y{year} S{semester})")
                except Exception as e:
                    errors += 1
                    print(f"Error creating {course_code} - {unit_code}: {e}")
    
    conn.commit()
    cur.close()
    conn.close()
    
    print(f"\n=== Summary ===")
    print(f"Units created: {units_created}")
    print(f"Units skipped (already exist): {units_skipped}")
    print(f"Errors: {errors}")

